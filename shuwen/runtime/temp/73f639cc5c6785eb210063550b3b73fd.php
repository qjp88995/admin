                <h4>简　介</h4>
                <div style="text-indent: 2em;">
                    <p>我国是非物质文化遗产大国。五千年的古老文明，漫长的农耕文化历史及56个民族多元化的文化生态，显现了中华民族民间文化艺术资源十分丰富及天才的艺术创造，有极高的历史价值，许多种类或世界独有，或世界第一。</p>
                    <p>玉屏箫笛，原名平箫玉笛，是我国著名竹管乐器中的两种。创于明代末期，采用贵州玉屏生长的竹子制成，因名。作为侗乡传统手工艺品和民族乐器代表，玉屏箫笛制作精良，其工艺精细、音色上佳。今考其以箫为业，世代相传的有郑家、刘氏、姚氏、侯氏、杨氏等仍然传袭箫笛制作技艺，取材于当地节长均匀、厚薄适宜的小水竹，经制坯、雕刻、成品3个流程，共计70多道工序才能完成。品种也有创新，由一箫一笛发展到七箫十二笛共100多个花色品种。</p>
                    <p>箫笛博物馆，是全国唯一的箫笛文化专题博物馆，位于贵州省铜仁市玉屏自治县。玉屏箫笛博物馆的设立，对更好地保护、研究玉屏箫笛非物质文化遗产，举办箫笛精品展览，开展社会宣传教育活动，弘扬民族文化、丰富人民群众文化生活，进一步打造和提升玉屏“箫笛之乡”的文化品牌具有重要的意义。</p>
